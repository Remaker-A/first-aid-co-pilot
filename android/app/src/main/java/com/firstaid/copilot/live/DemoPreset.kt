    currentRate: Int,
    averageRate: Int,
    qualityScore: Int,
    handPosition: String,
    armPosture: String,
    interruptionSeconds: Int,
    totalCompressions: Int,
    compressionsStarted: Boolean,
): Map<String, Any?> =
    mapOf(
        "compressions_started" to compressionsStarted,
        "current_rate" to currentRate,
        "average_rate" to averageRate,
        "quality_score" to qualityScore,
        "hand_position" to handPosition,
        "arm_posture" to armPosture,
        "interruption_seconds" to interruptionSeconds,
        "total_compressions" to totalCompressions,
    )
