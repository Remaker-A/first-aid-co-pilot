data class DemoPreset(
    val id: String,
    val label: String,
    val summary: String,
    val eventSource: String? = null,
    val eventType: String? = null,
    val patientState: Map<String, Any?>? = null,
    val cprQuality: Map<String, Any?>? = null,
    val rescuerState: Map<String, Any?>? = null,
    val deviceState: Map<String, Any?>? = null,
    val metadata: Map<String, Any?>? = null,
    val toolResult: Map<String, Any?>? = null,
    val requiresCprLoop: Boolean = false,
    val requiresCprSetup: Boolean = false,
    val bestAfterCprSetup: Boolean = false,
)

data class DemoTurn(
    val preset: DemoPreset,
    val text: String? = null,
)

